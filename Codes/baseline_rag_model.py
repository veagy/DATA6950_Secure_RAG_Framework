    inputs = llama_tok(check_prompt, return_tensors="pt").to("cuda")

    # Keep max_new_tokens very low for speed and consistency
    out = llama_model.generate(
        **inputs,
        max_new_tokens=2,
        do_sample=False,
        pad_token_id=llama_tok.eos_token_id
    )

    verdict = llama_tok.decode(out[0], skip_special_tokens=True).split("assistant")[-1].strip().upper()
    return "UNSAFE" not in verdict

