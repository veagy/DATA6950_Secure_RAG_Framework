def generate(best_doc, query):
    """Produces the final answer using the Llama 3.2 Instruct model."""
    prompt = f"<|begin_of_text|><|start_header_id|>system<|end_header_id|>\nAnswer ONLY from context.'.<|eot_id|><|start_header_id|>user<|end_header_id|>\n\nContext: {best_doc}\n\nQuestion: {query}<|eot_id|><|start_header_id|>assistant<|end_header_id|>"
    inputs = llama_tok(prompt, return_tensors="pt").to("cuda")
    out = llama_model.generate(**inputs, max_new_tokens=50, do_sample=False, pad_token_id=llama_tok.eos_token_id)
    return llama_tok.decode(out[0], skip_special_tokens=True).split("assistant")[-1].strip()

"""# End to End Pipeline"""

# --- STEP 5: End-to-End RAG Pipeline ---
def rag_pipeline(query, k=10):
    """
    Standard RAG Workflow:
    1. Retrieval: Fetch candidates from FAISS
    2. Reranking: Use Cross-Encoder to find the single best context
    3. Generation: Use Llama 3.2 to answer based on that context
    """
    # 1. Retrieval
    qvec = embed_model.encode([query], normalize_embeddings=True).astype("float32")
    _, indices = index.search(qvec, k)
    hits = [documents[i] for i in indices[0]]

    # 2. Reranking
    pairs = [[query, h] for h in hits]
    inputs = rerank_tok(pairs, return_tensors="pt", padding=True, truncation=True).to("cuda")
    with torch.no_grad():
        scores = rerank_model(**inputs).logits.squeeze(-1)
    best_doc = hits[torch.argmax(scores).item()]

    # 3. Generation
    prompt = f"<|begin_of_text|><|start_header_id|>system<|end_header_id|>\nAnswer ONLY from context. If unsure, say 'I do not know'.<|eot_id|><|start_header_id|>user<|end_header_id|>\n\nContext: {best_doc}\n\nQuestion: {query}<|eot_id|><|start_header_id|>assistant<|end_header_id|>"

    inputs = llama_tok(prompt, return_tensors="pt").to("cuda")
    out = llama_model.generate(
        **inputs,
        max_new_tokens=50,
        do_sample=False,
        pad_token_id=llama_tok.eos_token_id
    )

    gen_answer = llama_tok.decode(out[0], skip_special_tokens=True).split("assistant")[-1].strip()

    return {
        "answer": gen_answer,
        "best_context": best_doc,
        "retrieved_contexts": hits
    }

