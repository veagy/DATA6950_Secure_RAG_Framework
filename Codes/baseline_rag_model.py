def rerank(query, hits):
    pairs = [[query, h] for h in hits]

    inputs = rerank_tok(
        pairs,
        padding=True,
        truncation=True,
        return_tensors="pt"
    ).to("cuda")

    with torch.no_grad():
        scores = rerank_model(**inputs).logits.squeeze()

    best_idx = torch.argmax(scores).item()
    return hits[best_idx]

